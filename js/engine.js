
function generateArticle(day){

  const e = generateEvent();
  const impacts = spread(e);
  const time = getTime(day);

  let title = `空島跨區域監測報導：${e.place}${e.type}引發局部變動`;

  /* =========================
  🧠 ① 導言（拉長版）
  ========================= */

  let intro = `
在${time}的監測週期中，空島觀測系統持續對全域節點進行動態追蹤。
本次於${e.place}首次偵測到明顯${e.type}現象，該變化在初期僅呈現局部波動，但在短時間內開始影響周邊物流與市場結構。

根據系統紀錄，該類型事件通常與區域資源流動重分配有關，尤其在交易活躍期更容易被放大。
`;

  /* =========================
  🌍 ② 系統反應（加厚）
  ========================= */

  let system = `
隨著事件進一步發展，物流與市場系統開始出現多層級調整。

首先，空鷹航線在進出${e.place}時出現短時間等待現象，導致部分補給節點被迫調整進場順序。
其次，市場交易節奏開始變得不穩定，部分高頻交易區出現短暫壅塞。

系統分析指出，此類波動通常會優先影響高流量節點，再逐步向外擴散至中型村落與次級市場。
`;

  /* =========================
  🚧 ③ 擴散（加細節）
  ========================= */

  let spreadText = `
目前影響已開始向外延伸，形成明顯的層級擴散結構：

・第一層影響區：${impacts[0]}、${impacts[1]}
　已出現市場節奏加快與補給延遲交錯現象

・第二層影響區：${impacts[2]}
　開始出現人流重新分布，部分居民改變交易時間

・第三層觀測區：${impacts[3] || impacts[0]}
　僅觀測到輕微連動跡象，目前仍處於早期變化階段
`;
  
  /* =========================
  🧍 ④ 現場描述（新聞關鍵升級）
  ========================= */

  let scene = `
現場回報顯示，${e.place}主要市場區在上午時段出現明顯人潮集中。
魚乾與布料攤位的交易等待時間延長，部分居民改為在非高峰時段進行交易。

水井補給點亦出現短時間排隊現象，導致部分家庭補給節奏延後。
空鷹補給隊表示航線進場時間增加，但整體仍可維持正常運作。
`;

  /* =========================
  📊 ⑤ 分析（變厚）
  ========================= */

  let analysis = `
從系統角度分析，本次事件屬於「局部壓力擴散型變動」。

其特徵如下：
第一，單一節點觸發後迅速影響交易密度。
第二，補給系統優先出現延遲，而非完全中斷。
第三，影響會沿物流路線呈階段性擴散。

歷史數據顯示，類似事件通常在1至3個空島日內逐步回穩，但若同時疊加市場波動週期，可能延長至更長觀測週期。
`;

  /* =========================
  🌍 ⑥ 世界結論（升級）
  ========================= */

  let conclusion = `
目前空島整體仍維持穩定結構。
雖然${e.type}已對局部區域造成明顯影響，但系統調節機制已開始介入，透過重新分配航線與補給節點進行壓力緩解。

觀測單位指出，本次事件尚未形成跨區崩解結構，但已具備進一步擴散條件。
後續24至48小時將是關鍵觀測期。
`;

  /* =========================
  📰 合成（重點：加換行讓文章變長）
  ========================= */

  return `
    <div class="news-title">${title}</div>

    <div class="news-body">

      <p>${intro}</p>

      <p>${system}</p>

      <p>${spreadText}</p>

      <p>${scene}</p>

      <p>${analysis}</p>

      <p>${conclusion}</p>

    </div>
  `;
}
